# Operators

num1 = 10
num2 = 12

# Arithmetic Operators
print("-----Arithmetic-----")
print("num1 + num2 is ", num1 + num2)
print("num1 - num2 is ", num1 - num2)
print("num1 * num2 is ", num1 * num2)
print("num1 / num2 is ", num1 / num2)
print("num1 ** num2 is ", num1 ** num2)
print("num1 // num2 is ", num1 // num2)
print("num1 % num2 is ", num1 % num2)
print("-----X-----")

# Assignment Operators
print("-----Assignment-----")
a = 10
a += 10
print(a)
a -= 10
print(a)
a *= 10
print(a)
a /= 10
print(a)
print("-----X-----")
# Comparison Operators
x = 10
y = 11
print("-----Comparison-----")
print(x > y)
print(x < y)
print(x >= y)
print(x <= y)
print(x == y)
print("-----X-----")

print("-----Logical-----")
print(x == y and x > y)
print(x < y or x > y)
print(not(False))
print(not(True))
print("-----X-----")


