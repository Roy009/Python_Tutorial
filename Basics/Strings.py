name1 = "Rohit"
name2 = 'Rohit Roy'
# Strings
print(name1)
print(type(name1))
print(name2)
print(type(name2))

# Slicing
print(name2[::3])
print(name1[:3])
print(name2[::-1])
# String methods
print(name1.upper())
print(name2.lower())
print(name1.join(name2))
print(name2.capitalize())
print(name2.count('R'))


